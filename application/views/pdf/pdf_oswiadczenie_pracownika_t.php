<p align="center"><h1>Oświadczenie o zachowaniu poufności</h1></p></p>


<p align="left">	Oświadczam, iż zostałam/zostałem* zapoznana/zapoznany* z przepisami dotyczących ochrony danych osobowych, w szczególności ustawy z dnia 29 sierpnia 1997r. o ochronie danych osobowych (tj. Dz. U. z 2015 r., poz. 2135 z późn zm.), wydanych na jej podstawie aktów wykonawczych oraz wprowadzonych i wdrożonych do stosowania przez Administratora Danych Polityki Bezpieczeństwa oraz Instrukcji Zarządzania Systemem Informatycznym służącym do przetwarzania danych osobowych.
<br/><br/>

<strong>Zobowiązuję się do:</strong>
<ul>
<li>zachowania w tajemnicy danych osobowych do których mam lub będę miał/a dostęp w trakcie wykonywania czynności w obszarze przetwarzania danych</li>
<li>zgłaszania sytuacji (incydentów) naruszenia zasad ochrony danych osobowych Administratorowi Bezpieczeństwa Informacji lub bezpośredniemu przełożonemu </li>
</ul><br/>

<p align="left">Przyjmuję do wiadomości, iż postępowanie sprzeczne z powyższymi zobowiązaniami, może być uznane jako naruszenie przepisów karnych ww. ustawy o ochronie danych osobowych lub ustawy z dnia 23 kwietnia 1964 r. Kodeks cywilny (Dz. U. z 2014 r., poz. 121 z późn. zm).</p><br/>

