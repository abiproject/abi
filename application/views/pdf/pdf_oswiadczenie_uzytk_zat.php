<p align="center"><h1>Oświadczenie o zachowaniu poufności</h1></p></p>

<p align="left">	Oświadczam, iż zostałam/zostałem* zapoznana/zapoznany* z przepisami dotyczących ochrony danych osobowych, w szczególności ustawy z dnia 29 sierpnia 1997r. o ochronie danych osobowych (tj. Dz. U. z 2015 r., poz. 2135 z późn zm.), wydanych na jej podstawie aktów wykonawczych oraz wprowadzonych i wdrożonych do stosowania przez Administratora Danych Polityki Bezpieczeństwa oraz Instrukcji Zarządzania Systemem Informatycznym służącym do przetwarzania danych osobowych.
<br/><br/>
Jednocześnie oświadczam, iż jestem zatrudniony/zatrudniona* przez Pracodawcę na podstawie umowy o pracę zawartej w dniu ….................... r.<br/>

<br/><br/>

<strong>Zobowiązuję się do:</strong>
<ul>
<li>zachowania w tajemnicy danych osobowych, do których mam lub będę miał/a dostęp w związku z wykonywaniem zadań służbowych lub obowiązków pracowniczych</li>
<li>niewykorzystywania danych osobowych w celach pozasłużbowych o ile nie są one jawne</li>
<li>zachowania w tajemnicy sposobów zabezpieczenia danych osobowych o ile nie są one jawne</li>
<li>przestrzegania regulaminu ochrony danych osobowych</li>
<li>korzystania z wyposażenia IT oraz oprogramowania wyłącznie w związku z wykonywaniem obowiązków pracowniczych</li>
<li>wykorzystywania jedynie legalnego oprogramowania pochodzącego od Pracodawcy</li>
<li>należytej dbałości o wyposażenie IT i oprogramowanie zgodnie z Instrukcją Zarządzania Systemem Informatycznym</li>
<li>korzystania z komputerów przenośnych zgodnie z  Instrukcją Zarządzania Systemem Informatycznym</li>
</ul><br/>

