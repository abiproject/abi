		<p align="Center"><font size="10">
			§ 1
		</font></p>
		<p align="Center"><font size="10"><b>
			Definicje
		</font></b></p>
		<p align="Left"><font size="10">
			1. Administrator Danych Osobowych - organ, jednostka organizacyjna, podmiot lub osoba, decydujące o celach i środkach przetwarzania danych osobowych,
		</font></p>
		<p align="Left"><font size="10">
			2. Zbiór danych - każdy posiadający strukturę zestaw danych o charakterze osobowym, dostępnych według określonych kryteriów, niezależnie od tego, czy zestaw ten jest rozproszony lub podzielony funkcjonalnie,
		</font></p>
		<p align="Left"><font size="10">
			3. Przetwarzanie danych - jakiekolwiek operacje wykonywane na danych osobowych, takie jak zbieranie, utrwalanie, przechowywanie, opracowywanie, zmienianie, udostępnianie i usuwanie, a zwłaszcza te, które wykonuje się w systemach informatycznych,
		</font></p>
		<p align="Left"><font size="10">
			4. Ustawa - Ustawa z dnia 29 sierpnia 1997 r. o ochronie danych osobowych (Dz. U. z 2015 r., poz. 2135 z późn. zm.),
		</font></p>
		<p align="Left"><font size="10">
			5. Rozporządzenie – Rozporządzenie Ministra Spraw Wewnętrznych i Administracji z dnia 29 kwietnia 2004 r. w sprawie dokumentacji przetwarzania danych osobowych oraz warunków technicznych i organizacyjnych, jakim powinny odpowiadać urządzenia i systemy informatyczne służące do przetwarzania danych osobowych (Dz.U. Nr 100, poz. 1024 z późn. zm.),
		</font></p>
		<p align="Left"><font size="10">
			6. GIODO – Generalny Inspektor Danych Osobowych
		</font></p>
