			4. Upoważnienie umożliwia Zleceniobiorcy przetwarzanie danych osobowych udostępnionych przez Zleceniodawcę wyłącznie w celu realizacji:.......................... w okresie niezbędnym dla jej realizacji.
		</font></p>
		<p align="Center"><font size="10">
			§ 3
		</font></p>
		<p align="Center"><font size="10"><b>
			Zobowiązania Zleceniobiorcy dotyczące ochrony danych osobowych
		</font></b></p>
		<p align="Left"><font size="10">
			1. Zleceniobiorca zobowiązuje się przetwarzać dane osobowe do których uzyskał upoważnienie wyłącznie w zakresie i celu przewidzianym w Umowie.
		</font></p>
		<p align="Left"><font size="10">
			2. Zleceniobiorca zobowiązuje się do zapewnienia poufności danych osobowych przetwarzanych w związku z wykonywaniem Umowy a w szczególności do tego, że nie będzie przekazywać, ujawniać i udostępniać tych danych osobom nieuprawnionym.
		</font></p>
		<p align="Left"><font size="10">
			3. Zleceniobiorca zobowiązuje się do przetwarzania danych osobowych wyłącznie w systemach informatycznych Zleceniodawcy. Zabraniania się jakiegokolwiek kopiowania i utrwalania tych danych poza systemami informatycznymi Zleceniodawcy.
		</font></p>
		<p align="Left"><font size="10">
			4. Zleceniobiorca w związku z wykonywaniem Umowy jest zobowiązany do przestrzegania zasad bezpieczeństwa ochrony danych osobowych opisanych w Polityce bezpieczeństwa Zleceniodawcy, Ustawie i Rozporządzeniu.
		</font></p>
		<p align="Left"><font size="10">
			5. Zleceniobiorca ponosi odpowiedzialność za będące następstwem jego zachowań szkody wyrządzone niezgodnym z Umową przetwarzaniem danych osobowych, w szczególności szkody wyrządzone udostępnieniem osobom nieupoważnionym, zabraniem przez osobę nieuprawnioną, przetwarzaniem z naruszeniem Ustawy oraz zmianą, utratą, uszkodzeniem lub zniszczeniem.
		</font></p>
		<p align="Center"><font size="10">
			§ 4
		</font></p>
		<p align="Center"><font size="10"><b>
			Zasady nadawania upoważnień
		</font></b></p>
		<p align="Left"><font size="10">
			1. Zleceniobiorca zobowiązuje się do dopuszczania do przetwarzania danych wyłącznie osoby posiadające upoważnienie nadane przez Zleceniobiorcę, zgodnie z następującymi punktami:
		</font></p>
		<p align="Left"><font size="10">
			Opcja 1 (scenariusz 1, gdy Zleceniobiorca w pełni zarządza swoimi pracownikami)<br />
			a) Zleceniobiorca przeprowadza szkolenie z zasad ochrony danych osobowych Zleceniodawcy i jeśli to ustalone,  zgodnie z wytycznymi przekazanymi przez Zleceniodawcę<br />
			b) Zleceniobiorca nadaje upoważnienia pracownikom przeznaczonym do przetwarzania danych osobowych w ramach realizacji warunków Umowy.<br />
			c) Zleceniobiorca zobowiązuje się do uzyskania od osób upoważnionych pisemnych, imiennych Oświadczeń o poufności<be />
			d) Zleceniobiorca zobowiązuje się do przekazywania aktualnej listy osób upoważnionych do Zleceniodawcy
		</font></p>
		<p align="Left"><font size="10">
			Opcja 2 (scenariusz 2, gdy dostępem do przetwarzania osób zatrudnionych przez Zleceniobiorcę zarządza Zleceniodawca)<br />
			a) Zleceniodawca zastrzega sobie prawo nadawania upoważnień pracownikom Zleceniobiorcy i prowadzenia ich aktualnej ewidencji<br />
			b) Zleceniodawca przeprowadza szkolenie z zasad ochrony danych osobowych, zgodnie z Polityką bezpieczeństwa Zleceniobiorcy dla osób, którym nadaje upoważnienia <br />
			c) Zleceniodawca zastrzega sobie uzyskania od osób upoważnionych pisemnych, imiennych Oświadczeń o poufności<br />
			d) Zleceniodawca zobowiązuje się do przekazywania aktualnej listy osób upoważnionych  do Zleceniobiorcy
		</font></p>
		<p align="Left"><font size="10">
			2. Do upoważniania osób stosuje się Załącznik nr 1.
		</font></p>
		<p align="Center"><font size="10">
			§ 5 *- paragraf nieobligatoryjny
		</font></p>
		<p align="Center"><font size="10"><b>
			Odpowiedzialności i kary
		</font></b></p>
		<p align="Left"><font size="10">
			1. W przypadku, gdy Umowa uprawnia Zleceniobiorcę do jej wykonywania przy udziale osób trzecich, postanowienia paragrafów poprzedzających rozciągają się również na te osoby, przy czym Zleceniobiorca odpowiada za działania lub zaniechania osób, którymi się posługuje lub którym powierza wykonanie niniejszej Umowy, jak za działania lub zaniechania własne.
		</font></P>
		<p align="Left"><font size="10">
			2. W przypadku naruszenia przepisów Ustawy w ramach realizacji Umowy z przyczyn leżących po stronie Zleceniobiorcy, w następstwie którego Zleceniodawca zostanie zobowiązany do wypłaty odszkodowania lub ukarany grzywną, prawomocnym wyrokiem lub decyzją właściwego organu, Zleceniobiorca zobowiązuje się do zwrócenia równowartości odszkodowania lub grzywny poniesionych przez Zleceniodawcę.
		</font></p>
